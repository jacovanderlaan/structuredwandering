def build_jsonld(title, subtitle, canonical, image, destination):
    data = {
        "@context": "https://schema.org", "@type": "Article",
        "headline": title, "description": subtitle, "url": canonical,
        "about": {"@type": "Place", "name": destination},
        "author": [{"@type": "Person", "name": "Jaco van der Laan"},
                   {"@type": "Person", "name": "Annemarie van der Laan"}],
        "publisher": {"@type": "Organization", "name": "Structured Wandering",
                      "url": BASE_URL},
    }
    if image:
        data["image"] = image
    return json.dumps(data, indent=2, ensure_ascii=False)


def render_header_image(hdr: dict | None) -> str:
    if not hdr:
        return ""
    credit = ""
    if hdr.get("photographer"):
        credit = (f'<figcaption>Photo: <a href="{esc(hdr.get("photographer_url"))}" '
                  f'rel="nofollow noopener">{esc(hdr["photographer"])}</a>'
                  f'{" · " + esc(hdr.get("provider")) if hdr.get("provider") else ""}</figcaption>')
    return (f'<figure class="hero-figure"><img src="{esc(hdr.get("url_full"))}" '
            f'alt="{esc(hdr.get("alt_text"))}">{credit}</figure>')


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--content", default=str(CONTENT_JSON),
                    help="brain export JSON (default: curated/content.json)")
    ap.add_argument("--draft", action="store_true",
                    help="also build status=draft pages (preview; not for publish)")
    args = ap.parse_args()

    src = Path(args.content)
    if not src.is_file():
        raise SystemExit(
            f"brain export not found: {src}\n"
            f"  Produce it in the travelbrain repo:\n"
            f"    python site/scripts/export_content.py   # -> site/src/content.json\n"
            f"  then copy it to {CONTENT_JSON} (ADR-085 phase 3 formalises this bridge).")

    data = json.loads(src.read_text(encoding="utf-8"))
    pages = data.get("pages") if isinstance(data, dict) else data
    if not pages:
        raise SystemExit("brain export has no pages")

    allowed = set(PUBLISH_STATUS) | ({"draft"} if args.draft else set())
    built = held = 0
    for pg in pages:
        status = (pg.get("status") or "").lower()
        if status not in allowed:
            continue
        fm = pg.get("frontmatter") or {}
        destination = fm.get("destination") or pg.get("title") or "Somewhere"
        slug = slugify(fm.get("path") or destination)

        ok, why = is_complete(pg)
        if not ok and not args.draft:
            print(f"  held back: {slug} ({why})")
            held += 1
            continue

        title = pg.get("title") or destination
        subtitle = fm.get("subtitle") or f"Chosen, not found — {destination} for adults."
        canonical = f"{BASE_URL}/{slug}/"
        hdr = pg.get("header_image") or {}
        og = hdr.get("url_full") or ""
        venues_html = "\n".join(render_venue(v) for v in (pg.get("venues") or []))

        out_dir = HERE / slug
        out_dir.mkdir(parents=True, exist_ok=True)
        (out_dir / "index.html").write_text(PAGE.format(
            title=esc(title), subtitle=esc(subtitle), destination=esc(destination),
            canonical=canonical, og_image=esc(og),
            json_ld=build_jsonld(title, subtitle, canonical, og, destination),
            header_image=render_header_image(pg.get("header_image")),
            body=md_to_html(pg.get("body_md") or ""),
            method=render_method(pg),
            venues=venues_html,
            pois=render_pois(pg.get("pois") or []),
        ), encoding="utf-8")
        n_v = len(pg.get("venues") or [])
        aff = sum(1 for v in pg.get("venues") or []
                  if (v.get("link") or {}).get("kind") == "affiliate")
        print(f"  + {slug}/index.html  ({n_v} venues, {aff} affiliate link(s), status={status})")
        built += 1

    print(f"built {built} destination(s)" + (f", held back {held}" if held else ""))


if __name__ == "__main__":
    main()
